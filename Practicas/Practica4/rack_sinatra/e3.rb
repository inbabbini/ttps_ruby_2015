# ----------------------------------------------------------
# Que es un DSL?
# ----------------------------------------------------------
# En el contexto de la informatica, una DSL (Domain Specific
# Language) es un lenguaje de programación (o diseño) que
# se especializa en un dominio de aplicacion especifico.
# Estos lenguajes ad-hoc son creados especificamente para
# resolver problemas particulares a su dominio de aplicacion
# mas alla de que se pueda utilizar con otros propositos.
# En general las DSLs se utilizan a modo de libreria, ya
# que suelen extender algun lenguaje y mantienen una
# coherencia con este a la hora de definir su sintaxis.

# ----------------------------------------------------------
# Que es Sinatra?
# ----------------------------------------------------------
# Sinatra se define como "DSL para crear aplicaciones web".
# Sabiendo que es una DSL, podemos interpretar que Sinatra
# define un lenguaje simple y consiso en el contexto de Ruby
# orientado al dominio de aplicaciones web, con el objetivo
# de facilitar una construcción con un esfuerzo minimo.
