# es que no hacen calculos ni trabajo de mas, solo lo que necesita
# En el caso de los enumeradores, los enumeradores lazy iran devolviendo
# resultados (supuestos elementos de la coleccion que manejan, SUPUESTAMENTE)
# a medida que se los van requiriendo, es decir, ON DEMAND
