# TDD o Test Driven Development es una tecnica de desarrollo que consiste
# en 3 simples pasos:
#  - Escribir una serie de TESTS (que inicialmente fallaran) los cuales
#    abarquen y definan una cierta funcionalidad (una interfaz, por ejemplo)
#  - Escribir la minima funcionalidad para que dichos tests pasen
#  - Refactorizar el codigo escrito para mejorarlo y adaptarlo a estandares

# La mayor diferencia con el metodo tradicional de escribir y luego testear
# es que justamente, si se plantearon bien los tests, muchos de los errores
# ya se van a tener en cuenta a la hora de desarrollar. Tambien es ventajoso
# porque se tiende a desarrollar (y testear) de manera desacoplada
